export const MOCK_BUNDLE: IntakeBundle = {
  repo,
  issues,
  pullRequests,
  viewerLogin: VIEWER,
};

/**
 * Returns a curated bundle. When the user analyzes a different repo in mock
 * mode we relabel the repo so the demo still reflects their input, while the
 * issues/PRs remain the rich curated set.
 */
export function mockBundleFor(owner: string, name: string): IntakeBundle {
  const isFlagship =
    `${owner}/${name}`.toLowerCase() === repo.fullName.toLowerCase();
  if (isFlagship) return MOCK_BUNDLE;
  return {
    ...MOCK_BUNDLE,
    repo: {
      ...repo,
      fullName: `${owner}/${name}`,
      htmlUrl: `https://github.com/${owner}/${name}`,
    },
  };
}
